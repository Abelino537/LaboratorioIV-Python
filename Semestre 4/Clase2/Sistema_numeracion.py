# Profundizando en los sistemas de numeración

# Sistema Decimal
a = 10
print(f'a Decimal: {a}')

# Sistema Binario
a = 0b1010
print(f'a Binario: {a}')

# Sistema Octal
a = 0o12
print(f'a Octal: {a}')

# Sistema Hexadecimal
a = 0xA
print(f'a Hexadecimal: {a}')

# Conversión de valores

# Base Decimal
a = int('23', 10);
print(f'a base Decimal: {a}')

# Base Binario
a = int('10111', 2)
print(f'a base Binario: {a}')

# Base Octal
a = int('27', 8)
print(f'a base Octal: {a}')

# Base Hexadecimal
a = int('17', 16)
print(f'a base Hexadecimal: {a}')

# Base 5 de 0 a 4
a = int('43', 5)
print(f'a base 5: {a}')

